#include "Weapon.hpp"
#include "HumanA.hpp"
#include "HumanB.hpp"

int	main(void)
{
	{
		Weapon club = Weapon("crude spiked club");
		HumanA bob("Bob", club);
		bob.attack();
		club.setType("some other type of club");
		bob.attack();
	}
	{
		Weapon club = Weapon("3.5.7");
		HumanB jim("Jim");
		jim.attack();
		jim.setWeapon(club);
		club.setType("machette");
		jim.attack();
	}
	return (0);
}